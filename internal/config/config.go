// AgeGateConfig holds age verification gate settings.
// When enabled, visitors must confirm they are 18+ before accessing content.
// Verification is cookie-primary; verified IPs are also cached with a TTL so
// users are not re-prompted if they clear cookies within the TTL window.
// Configured CIDR bypass ranges skip the gate entirely (e.g. admin LAN IPs).
type AgeGateConfig struct {
	Enabled      bool          `json:"enabled"`
	BypassIPs    []string      `json:"bypass_ips"`     // CIDRs that skip the gate (e.g. "192.168.0.0/24")
	IPVerifyTTL  time.Duration `json:"ip_verify_ttl"`  // How long a verified IP is trusted (default: 24h)
	CookieName   string        `json:"cookie_name"`    // Cookie name (default: "age_verified")
	CookieMaxAge int           `json:"cookie_max_age"` // Cookie max-age in seconds (default: 31536000)
}

// UpdaterConfig holds settings for the source-based updater.
// When AppDir and DeployKeyPath are set, the server can perform
// git-pull-based updates instead of downloading GitHub release assets.
type UpdaterConfig struct {
	// AppDir is the path to the application's git repository on disk.
	// If empty, the updater falls back to the directory of the running binary.
	AppDir string `json:"app_dir"`

	// DeployKeyPath is the path to the Ed25519 SSH private key used to
	// authenticate git operations against a private GitHub repository via SSH.
	// Generated by install-cloudpanel.sh when MSP_PRIVATE_REPO=true.
	DeployKeyPath string `json:"deploy_key_path"`

	// GitHubToken is a GitHub personal access token (PAT) or fine-grained token.
	// When set it is used to:
	//   - Authenticate GitHub API calls (allows access to private repos / higher rate limits)
	//   - Authenticate HTTPS git fetch/pull from private repositories
	// Use UPDATER_GITHUB_TOKEN in .env to avoid storing the token in config.json.
	GitHubToken string `json:"github_token"`

	// GitHubUsername is the GitHub account name paired with GitHubToken for HTTPS auth.
	// When set alongside GitHubToken, git and go mod operations authenticate via
	// https://username:token@github.com/ instead of the Authorization header approach,
	// which is more broadly compatible with older git versions and private Go modules.
	// Use UPDATER_GITHUB_USERNAME in .env.
	GitHubUsername string `json:"github_username"`

	// Branch is the git branch to track (default: "main").
	Branch string `json:"branch"`

	// UpdateMethod controls how the admin panel applies updates:
	//   "source"  — git pull + npm build + go build (default)
	//   "binary"  — download pre-built binary from GitHub Releases
	// Use UPDATER_METHOD in .env.
	UpdateMethod string `json:"update_method"`
}

