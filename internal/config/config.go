// CreateDirectories ensures all configured directories exist
func (m *Manager) CreateDirectories() error {
	m.mu.RLock()
	defer m.mu.RUnlock()

	dirs := []string{
		m.config.Directories.Videos,
		m.config.Directories.Music,
		m.config.Directories.Thumbnails,
		m.config.Directories.Playlists,
		m.config.Directories.Uploads,
		m.config.Directories.Analytics,
		m.config.Directories.HLSCache,
		m.config.Directories.Data,
		m.config.Directories.Logs,
		m.config.Directories.Temp,
	}

	for _, dir := range dirs {
		absDir, err := filepath.Abs(dir)
		if err != nil {
			return fmt.Errorf("failed to resolve path %s: %w", dir, err)
		}

		if err := os.MkdirAll(absDir, 0755); err != nil {
			return fmt.Errorf("failed to create directory %s: %w", absDir, err)
		}

		// Verify the path is actually a directory
		info, err := os.Stat(absDir)
		if err != nil {
			return fmt.Errorf("failed to stat directory %s: %w", absDir, err)
		}
		if !info.IsDir() {
			return fmt.Errorf("path exists but is not a directory: %s", absDir)
		}

		m.log.Debug("Ensured directory exists: %s", absDir)
	}

	return nil
}

// Validate validates the current configuration
func (m *Manager) Validate() []error {
	m.mu.RLock()
	defer m.mu.RUnlock()

	var errors []error

	// Validate server config
	if m.config.Server.Port < 1 || m.config.Server.Port > 65535 {
		errors = append(errors, fmt.Errorf("invalid server port: %d", m.config.Server.Port))
	}
	// Validate server timeouts to prevent resource exhaustion (log warnings, don't fail)
	if m.config.Server.ReadTimeout <= 0 {
		m.log.Warn("ReadTimeout is %v, timeouts disabled - may cause resource exhaustion", m.config.Server.ReadTimeout)
	}
	if m.config.Server.WriteTimeout <= 0 {
		m.log.Warn("WriteTimeout is %v, timeouts disabled - may cause resource exhaustion", m.config.Server.WriteTimeout)
	}
	if m.config.Server.IdleTimeout <= 0 {
		m.log.Warn("IdleTimeout is %v, timeouts disabled - may cause resource exhaustion", m.config.Server.IdleTimeout)
	}

	// Validate HTTPS config
	if m.config.Server.EnableHTTPS {
		if m.config.Server.CertFile == "" {
			errors = append(errors, fmt.Errorf("HTTPS enabled but no cert_file specified"))
		}
		if m.config.Server.KeyFile == "" {
			errors = append(errors, fmt.Errorf("HTTPS enabled but no key_file specified"))
		}
	}

	// Validate streaming config
	if m.config.Streaming.DefaultChunkSize < 1024 {
		errors = append(errors, fmt.Errorf("chunk_size too small: %d", m.config.Streaming.DefaultChunkSize))
	}

	// Validate admin config (warn only — missing credentials make the account inaccessible but don't prevent startup)
	if m.config.Admin.Enabled && m.config.Admin.Username == "" {
		m.log.Warn("admin enabled but no username specified — admin login will fail until ADMIN_USERNAME is set")
	}
	if m.config.Admin.Enabled && m.config.Admin.PasswordHash == "" {
		m.log.Warn("admin enabled but no password hash — admin login will fail until ADMIN_PASSWORD_HASH is set")
	}

	// Validate rate limiting
	if m.config.Security.RateLimitEnabled && m.config.Security.RateLimitRequests < 1 {
		errors = append(errors, fmt.Errorf("rate_limit_requests must be positive"))
	}

	// Validate HLS segment duration
	if m.config.HLS.Enabled && (m.config.HLS.SegmentDuration < 1 || m.config.HLS.SegmentDuration > 60) {
		errors = append(errors, fmt.Errorf("hls segment_duration must be between 1 and 60 seconds, got: %d", m.config.HLS.SegmentDuration))
	}

	// Database is required — all persistence uses MySQL
	if !m.config.Database.Enabled {
		errors = append(errors, fmt.Errorf("database must be enabled: set DATABASE_ENABLED=true"))
	} else {
		if m.config.Database.Host == "" {
			errors = append(errors, fmt.Errorf("database host is required"))
		}
		if m.config.Database.Port < 1 || m.config.Database.Port > 65535 {
			errors = append(errors, fmt.Errorf("invalid database port: %d", m.config.Database.Port))
		}
		if m.config.Database.Name == "" {
			errors = append(errors, fmt.Errorf("database name is required"))
		}
	}

	// Warn about wildcard CORS with auth enabled
	if m.config.Security.CORSEnabled && m.config.Auth.Enabled {
		for _, origin := range m.config.Security.CORSOrigins {
			if origin == "*" {
				m.log.Warn("SECURITY: Wildcard CORS origin (*) is active with authentication enabled. " +
					"This allows any website to make cross-origin requests with user cookies.")
				break
			}
		}
	}

	return errors
}

// GetValue gets a configuration value by dot-notation path
func (m *Manager) GetValue(path string) (interface{}, error) {
	m.mu.RLock()
	defer m.mu.RUnlock()

	parts := strings.Split(path, ".")
	v := reflect.ValueOf(m.config).Elem()

	for _, part := range parts {
		if v.Kind() == reflect.Struct {
			v = v.FieldByNameFunc(func(name string) bool {
				return strings.EqualFold(name, part)
			})
			if !v.IsValid() {
				return nil, fmt.Errorf(errConfigPathNotFoundFmt, path)
			}
		} else {
			return nil, fmt.Errorf(errConfigPathNotFoundFmt, path)
		}
	}

	return v.Interface(), nil
}

// normalizeFieldName strips underscores and lowercases for flexible field name matching.
// This allows snake_case paths like "auto_generate" to match CamelCase fields like "AutoGenerate".
func normalizeFieldName(name string) string {
	return strings.ToLower(strings.ReplaceAll(name, "_", ""))
}

// navigateToField traverses the config struct by dot-notation parts and returns the target field.
func navigateToField(root reflect.Value, parts []string, path string) (reflect.Value, error) {
	v := root
	for _, part := range parts {
		if v.Kind() != reflect.Struct {
			return reflect.Value{}, fmt.Errorf(errConfigPathNotFoundFmt, path)
		}
		normalized := normalizeFieldName(part)
		v = v.FieldByNameFunc(func(name string) bool {
			return normalizeFieldName(name) == normalized
		})
		if !v.IsValid() {
			return reflect.Value{}, fmt.Errorf(errConfigPathNotFoundFmt, path)
		}
	}
	return v, nil
}

// setReflectField sets a reflect field value with type conversion.
func setReflectField(field reflect.Value, value interface{}, path string) error {
	if !field.CanSet() {
		return fmt.Errorf("cannot set config value: %s", path)
	}
	newVal := reflect.ValueOf(value)
	if !newVal.Type().ConvertibleTo(field.Type()) {
		return fmt.Errorf("type mismatch for config value: %s", path)
	}
	field.Set(newVal.Convert(field.Type()))
	return nil
}

// SetValue sets a configuration value by dot-notation path
func (m *Manager) SetValue(path string, value interface{}) error {
	m.mu.Lock()
	defer m.mu.Unlock()

	parts := strings.Split(path, ".")
	field, err := navigateToField(reflect.ValueOf(m.config).Elem(), parts, path)
	if err != nil {
		return err
	}
	if err := setReflectField(field, value, path); err != nil {
		return err
	}
	return m.save()
}
