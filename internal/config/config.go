// findEnvFile looks for .env file in common locations
func (m *Manager) findEnvFile() string {
	// Check for .env in same directory as config file
	configDir := filepath.Dir(m.configPath)
	envPath := filepath.Join(configDir, ".env")
	if _, err := os.Stat(envPath); err == nil {
		return envPath
	}

	// Check current working directory
	if _, err := os.Stat(".env"); err == nil {
		return ".env"
	}

	// Check executable directory
	if exePath, err := os.Executable(); err == nil {
		exeDir := filepath.Dir(exePath)
		envPath = filepath.Join(exeDir, ".env")
		if _, err := os.Stat(envPath); err == nil {
			return envPath
		}
	}

	return ""
}

// stripEnvQuotes removes surrounding single or double quotes from a value.
// Handles basic escaped quotes within strings.
func stripEnvQuotes(value string) string {
	if len(value) < 2 {
		return value
	}

	// Check for matching outer quotes
	if value[0] == '"' && value[len(value)-1] == '"' {
		// Remove outer quotes
		unquoted := value[1 : len(value)-1]
		// Handle escaped double quotes within the string
		return strings.ReplaceAll(unquoted, `\"`, `"`)
	}

	if value[0] == '\'' && value[len(value)-1] == '\'' {
		// Single quotes don't support escape sequences in shell
		return value[1 : len(value)-1]
	}

	// No quotes to strip
	return value
}

// parseEnvLine parses a single KEY=VALUE line. Returns empty key for invalid lines.
func parseEnvLine(line string) (key, value string) {
	idx := strings.Index(line, "=")
	if idx == -1 {
		return "", ""
	}
	key = strings.TrimSpace(line[:idx])
	value = stripEnvQuotes(strings.TrimSpace(line[idx+1:]))
	return key, value
}

// loadEnvFile loads environment variables from a .env file
func (m *Manager) loadEnvFile(path string) error {
	file, err := os.Open(path)
	if err != nil {
		return err
	}
	defer func() {
		if err := file.Close(); err != nil {
			m.log.Warn("Failed to close .env file: %v", err)
		}
	}()

	scanner := bufio.NewScanner(file)
	lineNum := 0
	for scanner.Scan() {
		lineNum++
		line := strings.TrimSpace(scanner.Text())

		// Skip empty lines and comments
		if line == "" || strings.HasPrefix(line, "#") {
			continue
		}

		key, value := parseEnvLine(line)
		if key == "" {
			m.log.Debug("Skipping invalid line %d in .env: no '=' found", lineNum)
			continue
		}

		// Only set if not already set in environment (allows system env to override .env file)
		if os.Getenv(key) == "" {
			if err := os.Setenv(key, value); err != nil {
				m.log.Warn("Failed to set environment variable %s from .env: %v", key, err)
				// Continue processing other variables even if one fails
			} else {
				m.log.Debug("Set env from .env: %s", key)
			}
		}
	}

	if err := scanner.Err(); err != nil {
		return fmt.Errorf("error reading .env file: %w", err)
	}
	return nil
}

// Save saves the current configuration to file
func (m *Manager) Save() error {
	m.mu.Lock()
	defer m.mu.Unlock()
	return m.save()
}

func (m *Manager) save() error {
	data, err := json.MarshalIndent(m.config, "", "  ")
	if err != nil {
		return fmt.Errorf("failed to marshal config: %w", err)
	}

	// Write to temp file first for atomic write
	tempPath := m.configPath + ".tmp"
	if err := os.WriteFile(tempPath, data, 0600); err != nil {
		return fmt.Errorf("failed to write temp config: %w", err)
	}

	// On Windows, os.Rename fails if destination exists, so remove it first
	if _, err := os.Stat(m.configPath); err == nil {
		if err := os.Remove(m.configPath); err != nil {
			return fmt.Errorf("failed to remove old config: %w", err)
		}
	}

	// Rename for atomic update
	if err := os.Rename(tempPath, m.configPath); err != nil {
		if cleanupErr := os.Remove(tempPath); cleanupErr != nil {
			m.log.Warn("Failed to cleanup temp file: %v", cleanupErr)
		}
		return fmt.Errorf("failed to rename config: %w", err)
	}

	m.log.Info("Configuration saved to %s", m.configPath)
	return nil
}

// Get returns a copy of the current configuration, safe for concurrent use.
func (m *Manager) Get() *Config {
	m.mu.RLock()
	defer m.mu.RUnlock()
	return m.getCopy()
}

// getCopy creates a deep copy of the config without acquiring the lock
// Must be called while holding m.mu (RLock or Lock)
func (m *Manager) getCopy() *Config {
	cp := *m.config
	// Deep copy slices and nested slice fields to prevent shared mutation
	cp.Security.IPWhitelist = append([]string(nil), m.config.Security.IPWhitelist...)
	cp.Security.IPBlacklist = append([]string(nil), m.config.Security.IPBlacklist...)
	cp.Security.CORSOrigins = append([]string(nil), m.config.Security.CORSOrigins...)
	cp.Uploads.AllowedExtensions = append([]string(nil), m.config.Uploads.AllowedExtensions...)
	cp.HLS.QualityProfiles = append([]HLSQuality(nil), m.config.HLS.QualityProfiles...)
	cp.Auth.UserTypes = append([]UserType(nil), m.config.Auth.UserTypes...)
	cp.RemoteMedia.Sources = append([]RemoteSource(nil), m.config.RemoteMedia.Sources...)
	cp.MatureScanner.HighConfidenceKeywords = append([]string(nil), m.config.MatureScanner.HighConfidenceKeywords...)
	cp.MatureScanner.MediumConfidenceKeywords = append([]string(nil), m.config.MatureScanner.MediumConfidenceKeywords...)
	cp.AgeGate.BypassIPs = append([]string(nil), m.config.AgeGate.BypassIPs...)
	return &cp
}

// Update updates the configuration and notifies watchers.
// Watchers receive a deep-copied snapshot of the config taken under lock.
// The update and save are performed atomically under a single lock to ensure
// consistent state and to allow rollback on save failure.
func (m *Manager) Update(updater func(*Config)) error {
	m.mu.Lock()
	defer m.mu.Unlock()

	// Save original config for rollback in case save fails
	originalJSON, err := json.Marshal(m.config)
	if err != nil {
		return fmt.Errorf("failed to marshal original config for backup: %w", err)
	}

	// Apply updates to in-memory config
	updater(m.config)

	// Persist to disk while holding lock
	if err := m.save(); err != nil {
		// Rollback on save failure
		var rollbackConfig Config
		if unmarshalErr := json.Unmarshal(originalJSON, &rollbackConfig); unmarshalErr == nil {
			m.config = &rollbackConfig
			m.log.Warn("Config save failed, rolled back in-memory changes: %v", err)
		} else {
			m.log.Error("Config save failed AND rollback failed: save error=%v, rollback error=%v", err, unmarshalErr)
		}
		return err
	}

	// Take a snapshot copy for watchers (we hold the lock so getCopy is safe)
	cfg := m.getCopy()

	// Read watchers under lock to avoid racing with OnChange
	watchers := make([]func(*Config), len(m.watchers))
	copy(watchers, m.watchers)

	// Notify watchers with panic recovery
	for _, watcher := range watchers {
		w := watcher
		go func() {
			defer func() {
				if r := recover(); r != nil {
					m.log.Error("Config watcher panic recovered: %v", r)
				}
			}()
			w(cfg)
		}()
	}

	return nil
}

// OnChange registers a callback for configuration changes
func (m *Manager) OnChange(callback func(*Config)) {
	m.mu.Lock()
	defer m.mu.Unlock()
	m.watchers = append(m.watchers, callback)
}

// Environment variable helper functions for configuration overrides.

func envGetStr(keys ...string) string {
	for _, key := range keys {
		if val := os.Getenv(key); val != "" {
			return val
		}
	}
	return ""
}

func envGetBool(keys ...string) (bool, bool) {
	for _, key := range keys {
		if val := os.Getenv(key); val != "" {
			return strings.ToLower(val) == "true" || val == "1", true
		}
	}
	return false, false
}

func envGetInt(keys ...string) (int, bool) {
	for _, key := range keys {
		if val := os.Getenv(key); val != "" {
			if i, err := strconv.Atoi(val); err == nil {
				return i, true
			}
		}
	}
	return 0, false
}

func envGetInt64(keys ...string) (int64, bool) {
	for _, key := range keys {
		if val := os.Getenv(key); val != "" {
			if i, err := strconv.ParseInt(val, 10, 64); err == nil {
				return i, true
			}
		}
	}
	return 0, false
}

func envGetFloat64(keys ...string) (float64, bool) {
	for _, key := range keys {
		if val := os.Getenv(key); val != "" {
			if f, err := strconv.ParseFloat(val, 64); err == nil {
				return f, true
			}
		}
	}
	return 0, false
}

func envGetDuration(unit time.Duration, keys ...string) (time.Duration, bool) {
	for _, key := range keys {
		if val := os.Getenv(key); val != "" {
			if i, err := strconv.Atoi(val); err == nil {
				return time.Duration(i) * unit, true
			}
		}
	}
	return 0, false
}

func envGetDurationString(keys ...string) (time.Duration, bool) {
	for _, key := range keys {
		if val := os.Getenv(key); val != "" {
			if d, err := time.ParseDuration(val); err == nil {
				return d, true
			}
		}
	}
	return 0, false
}

